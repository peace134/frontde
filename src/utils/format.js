/**
 * 将 AI 返回的 Markdown 风格文本转为可读的 HTML
 * - ### / ## / # 标题 → 加粗显示
 * - **加粗** → <strong>加粗</strong>
 * - *斜体* → <em>斜体</em>
 * - `-` / `*` 列表项 → 缩进圆点
 * - 换行 → <br> / <p>
 * - 自动过滤危险 HTML 标签
 */
export function formatAI(text) {
  if (!text) return ''

  // 1. 先转义 HTML 实体防止 XSS
  let s = String(text)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')

  // 统一换行符
  s = s.replace(/\r\n/g, '\n')

  // ---------------------------------------------------------- #
  // 2. 行首标记处理（必须在换行→<br> 转换之前，否则 ^ 失效）
  // ---------------------------------------------------------- #

  // 2a. 标题：### → <strong>（加粗标题文字）
  s = s.replace(/^#{1,6}\s+(.*)$/gm, '<strong>$1</strong>')

  // 2b. 无序列表：- 或 * 开头 → 缩进圆点
  s = s.replace(/^(?:\s*)[-*]\s+(.*)$/gm, '&nbsp;&nbsp;• $1')

  // 2c. 有序列表：1. 2. 3. → 缩进保留数字
  s = s.replace(/^(\s*)(\d+)\.\s+(.*)$/gm, '$1$2. $3')

  // ---------------------------------------------------------- #
  // 3. 行内格式（不受换行影响）
  // ---------------------------------------------------------- #

  // **加粗**（优先于斜体）
  s = s.replace(/\*\*(.+?)\*\*/g, '<strong>$1</strong>')

  // *斜体*
  s = s.replace(/\*(.+?)\*/g, '<em>$1</em>')

  // 行内 `code`
  s = s.replace(/`([^`]+)`/g, '<code>$1</code>')

  // 代码块 ```...``` 替换为提示
  s = s.replace(/```[\s\S]*?```/g, '(代码片段)')

  // ---------------------------------------------------------- #
  // 4. 换行 → HTML
  // ---------------------------------------------------------- #

  // 连续两个及以上换行 → 段落分隔
  s = s.replace(/\n{2,}/g, '</p><p>')

  // 单个换行 → <br>
  s = s.replace(/\n/g, '<br>')

  // ---------------------------------------------------------- #
  // 5. 包裹 & 清理
  // ---------------------------------------------------------- #

  s = '<p>' + s + '</p>'

  // 清理空段落
  s = s.replace(/<p><\/p>/g, '')
  s = s.replace(/<p><br><\/p>/g, '')
  s = s.replace(/<p><br><\/p>/gi, '')

  return s
}

/**
 * 安全渲染 AI 文本（直接返回格式化后的 HTML 字符串）
 * 配合 v-html 使用：<div v-html="renderAI(text)" />
 */
export function renderAI(text) {
  return formatAI(text)
}